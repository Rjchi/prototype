    // CONSULTAS A LA BASE DE DATOS
    public void consulta(int numero) {
        
        try {
            cn = conexion.Conectar();
            st = cn.createStatement();
            rs = st.executeQuery("SELECT * FROM citacionescomites WHERE cit_comite_fk = '" + numero + "'");
            rs.next();
            cadena = "Asunto: " + rs.getString("cit_asunto") + "\n" + "Lugar: " + rs.getString("cit_lugar") + "\n" + "Hora: " + rs.getString("cit_hora") + "\n" + "Fecha: " + rs.getString("cit_fecha") + "\n";
            txtNotificacion.setText(cadena);
            rs = st.executeQuery("SELECT * FROM instructores WHERE ins_num = '" + numero + "'");
            rs.next();
            lblNombre.setText(rs.getString("ins_nombres") + " " + rs.getString("ins_apellidos"));
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Error al procesar la notificaci√≥n: " + e);
        }
    }
    
    // METODO PARA VER LA VENTANA EMERGENTE DE LAS NOTIFICACIONES
    public void notificacion() {
        Notification objeto = new Notification(this, WindowPosition.BOTTOMRIGHT, 0, 0, 6000);
        NotificationQueue ventana = new NotificationQueue();
        ventana.add(objeto);
    }
    
    
