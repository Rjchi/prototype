    public void consulta() {
        //METODO PARA EL REGISTRO DE OBSERVACIONES(tabla observacionescomites)
        obs_descripcion = txtSalida.getText();
        try {
            if (obs_descripcion.equals("")) {
                JOptionPane.showMessageDialog(null, "Sin Observaciones");
            } else {
                String sql = "INSERT INTO observacionescomites(obs_fecha, obs_descripcion, obs_comite_fk)VALUES('" + fecha + "', '" + obs_descripcion + "', '" + comite + "')";
                cn = conexion.Conectar();
                st = cn.createStatement();
                st.executeUpdate(sql);
                JOptionPane.showMessageDialog(null, "Enviado exitosamente");
                txtSalida.setText("");
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Error al enviar: " + e);
        }
    }

